# Python Program to calculate the square root

# Note: change this value for a different result
num = 8 

# uncomment to take the input from the user
# num = float(input('Enter a number: '))
num_sqrt = num ** 0.5
print('The square root of %0.0f is %f'%(num ,num_sqrt))        # %f identifica a variável como float e não limita as casas decimais; o 0.0 identifica
# 0 casas decimais, podendo ser 0.1 para uma casa decimal, etc; % variável ali depois é o % (num ,num_sqrt)
